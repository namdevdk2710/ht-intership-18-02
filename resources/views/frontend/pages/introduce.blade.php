        Bởi hàng năm, các
        tổ
        chức nhân đạo đã triển khai Chiến dịch Hiến máu tình nguyện, kêu gọi người dân tham gia hiến máu với con số đông
        và rộng khắp các vùng miền trên cả nước. Hiến máu nhân đạo đã trở thành nét đẹp truyền thống quý giá của dân
        tộc, thể hiện tinh thần nhân đạo cao cả, tình yêu thương con người, lòng nhân ái bao dung, biết sẻ chia những
        khó khăn giữa người với người trong cuộc sống.
        Máu được xem là một phần không thể thiếu của cơ thể con người, máu trong cơ thể người có chức năng cung cấp các
        chất nuôi dưỡng và cấu tạo các tổ chức cũng như loại bỏ các chất thải trong quá trình chuyển hóa của cơ thể. Có
        thể nói, máu là món quà vô giá giúp con người duy trì sự sống. Ngày nay khoa học, y học tuy đã có sự phát triển
        vượt bậc nhưng vẫn chưa có một loại thuốc hay một loại dịch thể nào có thể thay thế được máu. Bởi vậy, muốn cứu
        sống được người bệnh có nguy cơ mất máu cao cũng như trong điều trị các bệnh hiểm nghèo cần truyền máu không có
        cách nào khác con người cần được hiến tặng, chia sẻ những giọt máu hồng của mình để góp phần vào việc duy trì sự
        sống cho những người cần máu.<br>
