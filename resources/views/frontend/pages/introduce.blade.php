                mời đến để tư vấn sức khỏe, phòng chống bệnh tật. Ngoài ra, người hiến máu được bồi dưỡng và chăm sóc
                theo các
                quy định hiện hành, đặc biệt được cấp Giấy Chứng nhận hiến máu tình nguyện của Ban Chỉ đạo hiến máu nhân
